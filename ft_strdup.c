#include"libft.h"

char	*ft_strdup(const char *s)
{
	char	*word;
	int		len;
	int		i;

	len = ft_strlen(s);
	word = (char *)malloc (len + 1);
	if (!word)
		return (0);
	i = 0;
	while (i < len)
	{
		word[i] = s[i];
		i++;
	}
	word[i] = '\0';
	return (word);
}
